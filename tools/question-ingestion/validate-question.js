#!/usr/bin/env node
/**
 * Lints an ingestion script before it inserts anything.
 *
 *   node tools/question-ingestion/validate-question.js scripts/insert_atom_b5.js
 *
 * The target script must export (or have a top-level `const`) named QUESTIONS,
 * CHAPTER_ID, and DISPLAY_ID_PREFIX exactly as in insert-template.js.
 *
 * Catches the things humans miss most often:
 *   • Missing primary tag (tags[0]) — silently sinks the question to the bottom
 *     of the chapter in browse view
 *   • chapter_id / tag_id not present in taxonomy
 *   • PYQ missing legacy mirrors (examBoard / is_pyq / exam_source) — old
 *     read paths break
 *   • applicableExams[0] !== examBoard — drift between v2 and legacy
 *   • Display-ID prefix mismatch with chapter_id
 *   • $$...$$ LaTeX (oversized rendering)
 *   • Empty options for SCQ/MCQ, missing answer for NVT
 *
 * Exits 0 if clean, 1 if any error found. Use this in pre-commit hooks if you
 * want — `node tools/question-ingestion/validate-question.js <file>`.
 */

const fs = require('fs');
const path = require('path');
const Module = require('module');

// ─── Load taxonomy snapshot from this folder ────────────────────────────────
// The snapshot is generated by refresh-taxonomy-snapshot.js (which must be run
// on a full project clone). On the other Mac, we just read the JSON — no
// dependency on the project's TypeScript taxonomy file.
function loadTaxonomyIds() {
    const snapshotPath = path.resolve(__dirname, 'taxonomy-snapshot.json');
    if (!fs.existsSync(snapshotPath)) {
        throw new Error(
            `Cannot find taxonomy-snapshot.json next to validate-question.js.\n` +
            `On a system with the full project, run: node tools/question-ingestion/refresh-taxonomy-snapshot.js\n` +
            `Then copy the whole tools/question-ingestion/ folder over.`
        );
    }
    const snap = JSON.parse(fs.readFileSync(snapshotPath, 'utf-8'));
    return {
        chapters: new Set(snap.chapters),
        tags: new Set(snap.tags),
        tagsByChapter: snap.tagsByChapter || {},
        generatedAt: snap.generated_at,
    };
}

// Mirror of CHAPTER_PREFIX_MAP from app/api/v2/questions/route.ts. Update both
// places if a new chapter is added.
const CHAPTER_PREFIX_MAP = {
    ch11_atom: 'ATOM', ch11_bonding: 'BOND', ch11_chem_eq: 'CEQ', ch11_goc: 'GOC',
    ch11_hydrocarbon: 'HC', ch11_ionic_eq: 'IEQ', ch11_mole: 'MOLE', ch11_pblock: 'PB11',
    ch11_periodic: 'PERI', ch11_prac_org: 'POC', ch11_redox: 'RDX', ch11_thermo: 'THERMO',
    ch12_alcohols: 'ALCO', ch12_amines: 'AMIN', ch12_biomolecules: 'BIO',
    ch12_carbonyl: 'ALDO', ch12_coord: 'CORD', ch12_dblock: 'DNF', ch12_electrochem: 'EC',
    ch12_haloalkanes: 'HALO', ch12_kinetics: 'CK', ch12_pblock: 'PB12',
    ch12_salt: 'SALT', ch12_solutions: 'SOL',
};

// Counts $ that aren't escaped (i.e., not preceded by an odd number of \).
function countUnescapedDollars(s) {
    if (!s) return 0;
    let count = 0;
    for (let i = 0; i < s.length; i++) {
        if (s[i] !== '$') continue;
        let bs = 0;
        for (let j = i - 1; j >= 0 && s[j] === '\\'; j--) bs++;
        if (bs % 2 === 0) count++;
    }
    return count;
}

function validateQuestion(q, idx, ctx) {
    const errs = [];
    const prefix = `Q[${idx}]`;
    const m = q.metadata || {};

    // ─── Required structural fields ──────────────────────────────────────────
    if (!q.question_text?.markdown || q.question_text.markdown.length < 10) {
        errs.push(`${prefix} question_text.markdown missing or too short (< 10 chars)`);
    }
    if (!q.type) errs.push(`${prefix} type is required`);
    else if (!['SCQ', 'MCQ', 'NVT', 'AR', 'MST', 'MTC', 'SUBJ'].includes(q.type)) {
        errs.push(`${prefix} type='${q.type}' is not a valid value`);
    }

    // SCQ/MCQ must have options with exactly one or more correct
    if (q.type === 'SCQ' || q.type === 'MCQ') {
        if (!q.options || q.options.length < 2) {
            errs.push(`${prefix} ${q.type} needs at least 2 options`);
        } else {
            const correctCount = q.options.filter(o => o.is_correct).length;
            if (q.type === 'SCQ' && correctCount !== 1) {
                errs.push(`${prefix} SCQ must have exactly 1 correct option (found ${correctCount})`);
            }
            if (q.type === 'MCQ' && correctCount < 1) {
                errs.push(`${prefix} MCQ must have at least 1 correct option`);
            }
        }
    }
    if (q.type === 'NVT' && !q.answer) {
        errs.push(`${prefix} NVT requires an answer object (integer_value or decimal_value)`);
    }

    // ─── Solution ────────────────────────────────────────────────────────────
    if (!q.solution?.text_markdown || q.solution.text_markdown.length < 20) {
        errs.push(`${prefix} solution.text_markdown missing or too short (< 20 chars)`);
    }

    // ─── Metadata ────────────────────────────────────────────────────────────
    if (!m.tags || m.tags.length === 0) {
        errs.push(`${prefix} metadata.tags is empty — primary tag is required`);
    } else {
        if (!m.tags[0]?.tag_id) {
            errs.push(`${prefix} metadata.tags[0].tag_id is missing — primary tag required`);
        }
        const validTagsForChapter = ctx.taxonomy.tagsByChapter[ctx.chapterId] || [];
        for (let i = 0; i < m.tags.length; i++) {
            const t = m.tags[i];
            if (!t.tag_id) errs.push(`${prefix} tags[${i}].tag_id missing`);
            else if (!ctx.taxonomy.tags.has(t.tag_id)) {
                errs.push(`${prefix} tags[${i}].tag_id='${t.tag_id}' not found in taxonomy`);
            } else if (validTagsForChapter.length > 0 && !validTagsForChapter.includes(t.tag_id)) {
                // Tag exists in taxonomy but belongs to a different chapter — common
                // copy-paste mistake when batching across chapters.
                errs.push(`${prefix} tags[${i}].tag_id='${t.tag_id}' does not belong to chapter '${ctx.chapterId}'`);
            }
        }
    }

    if (typeof m.difficultyLevel !== 'number' || m.difficultyLevel < 1 || m.difficultyLevel > 5) {
        errs.push(`${prefix} metadata.difficultyLevel must be 1-5 (got ${m.difficultyLevel})`);
    }

    // ─── Exam taxonomy: v2 fields + legacy mirrors ──────────────────────────
    if (!Array.isArray(m.applicableExams) || m.applicableExams.length === 0) {
        errs.push(`${prefix} metadata.applicableExams must be a non-empty array`);
    }
    if (!m.sourceType) {
        errs.push(`${prefix} metadata.sourceType is required`);
    } else if (!['PYQ', 'NCERT_Textbook', 'NCERT_Exemplar', 'Practice', 'Mock'].includes(m.sourceType)) {
        errs.push(`${prefix} metadata.sourceType='${m.sourceType}' is not a valid value`);
    }

    // Legacy mirrors must match the v2 fields
    if (m.applicableExams && m.examBoard && m.applicableExams[0] !== m.examBoard) {
        errs.push(`${prefix} examBoard='${m.examBoard}' does not match applicableExams[0]='${m.applicableExams[0]}'`);
    }
    if (m.sourceType === 'PYQ' && m.is_pyq !== true) {
        errs.push(`${prefix} sourceType='PYQ' but is_pyq is not true (legacy mirror out of sync)`);
    }
    if (m.sourceType !== 'PYQ' && m.is_pyq === true) {
        errs.push(`${prefix} sourceType='${m.sourceType}' but is_pyq=true (legacy mirror out of sync)`);
    }

    // PYQs must have year + exam in examDetails AND exam_source
    if (m.sourceType === 'PYQ') {
        if (!m.examDetails?.year) errs.push(`${prefix} PYQ missing examDetails.year`);
        if (!m.examDetails?.exam) errs.push(`${prefix} PYQ missing examDetails.exam`);
        if (!m.exam_source?.year) errs.push(`${prefix} PYQ missing exam_source.year (legacy mirror)`);
        if (!m.exam_source?.exam) errs.push(`${prefix} PYQ missing exam_source.exam (legacy mirror)`);
    }
    // Non-PYQs must NOT have examDetails / exam_source
    if (m.sourceType && m.sourceType !== 'PYQ') {
        if (m.examDetails) errs.push(`${prefix} sourceType='${m.sourceType}' should not have examDetails`);
        if (m.exam_source) errs.push(`${prefix} sourceType='${m.sourceType}' should not have exam_source`);
    }

    // ─── LaTeX sanity ────────────────────────────────────────────────────────
    const allText = [
        q.question_text?.markdown,
        q.solution?.text_markdown,
        ...(q.options || []).map(o => o.text),
    ].filter(Boolean).join('\n');

    if (allText.includes('$$')) {
        errs.push(`${prefix} contains '$$' (double dollar) — use '$...$' inline only`);
    }
    if (countUnescapedDollars(allText) % 2 !== 0) {
        errs.push(`${prefix} odd number of '$' delimiters — unclosed inline math somewhere`);
    }

    return errs;
}

function main() {
    const target = process.argv[2];
    if (!target) {
        console.error('Usage: node tools/question-ingestion/validate-question.js <script-path>');
        process.exit(2);
    }
    const absPath = path.resolve(process.cwd(), target);
    if (!fs.existsSync(absPath)) {
        console.error(`File not found: ${absPath}`);
        process.exit(2);
    }

    // Load script's QUESTIONS / CHAPTER_ID / DISPLAY_ID_PREFIX without running its main()
    // by intercepting MongoClient.connect into a no-op via stub require resolution.
    // Simpler: just require() and ignore the side-effect (script awaits a connection
    // that fails fast under the validator since we set MONGODB_URI=invalid first).
    process.env.MONGODB_URI = process.env.MONGODB_URI || 'mongodb://invalid:0/x?serverSelectionTimeoutMS=1';
    let scriptModule;
    try {
        // Clear require cache for the file so re-runs work
        delete require.cache[absPath];
        scriptModule = require(absPath);
    } catch (e) {
        // Many scripts call main() at the end — Mongo connect will fail under the dummy URI,
        // but that's after the constants have already been registered. We just need the
        // module's exports (or top-level constants exposed via a thin export shim).
        if (!scriptModule) {
            console.error('Could not load script — make sure it exports QUESTIONS, CHAPTER_ID, DISPLAY_ID_PREFIX,');
            console.error('or convert top-level const declarations to: module.exports = { QUESTIONS, CHAPTER_ID, DISPLAY_ID_PREFIX }.');
            console.error('Underlying error:', e.message);
            process.exit(2);
        }
    }

    const { QUESTIONS, CHAPTER_ID, DISPLAY_ID_PREFIX } = scriptModule || {};
    if (!QUESTIONS || !CHAPTER_ID || !DISPLAY_ID_PREFIX) {
        console.error('Script must export QUESTIONS, CHAPTER_ID, DISPLAY_ID_PREFIX. Add at the bottom:');
        console.error('  module.exports = { QUESTIONS, CHAPTER_ID, DISPLAY_ID_PREFIX };');
        process.exit(2);
    }

    const taxonomy = loadTaxonomyIds();
    const ctx = { taxonomy, chapterId: CHAPTER_ID };
    console.log(`(taxonomy snapshot generated ${taxonomy.generatedAt})\n`);

    const allErrs = [];

    // Top-level checks
    if (!taxonomy.chapters.has(CHAPTER_ID)) {
        allErrs.push(`CHAPTER_ID='${CHAPTER_ID}' not found in taxonomy`);
    }
    const expectedPrefix = CHAPTER_PREFIX_MAP[CHAPTER_ID];
    if (expectedPrefix && DISPLAY_ID_PREFIX !== expectedPrefix) {
        allErrs.push(`DISPLAY_ID_PREFIX='${DISPLAY_ID_PREFIX}' does not match canonical '${expectedPrefix}' for chapter '${CHAPTER_ID}'`);
    }

    // Per-question checks
    QUESTIONS.forEach((q, i) => {
        validateQuestion(q, i, ctx).forEach(e => allErrs.push(e));
    });

    if (allErrs.length === 0) {
        console.log(`OK — ${QUESTIONS.length} question(s) pass validation`);
        console.log(`Chapter: ${CHAPTER_ID} | Prefix: ${DISPLAY_ID_PREFIX}`);
        process.exit(0);
    }

    console.error(`FAIL — ${allErrs.length} issue(s) found:\n`);
    allErrs.forEach(e => console.error('  ✗ ' + e));
    process.exit(1);
}

main();
